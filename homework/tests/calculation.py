"""The functions examples for testing."""


def add(num1: int, num2: int) -> int:
    """Add two provided numbers.

    :param num1: an integer for addition
    :param num2: an integer for addition
    :return: a sum result of the two provided numbers
    """
    return num1 + num2


def subtract(num1: int, num2: int) -> int:
    """Subtract two provided numbers.

    :param num1: an integer for subtraction
    :param num2: an integer for subtraction
    :return: a subtraction result of the two provided numbers
    """
    return num1 - num2
